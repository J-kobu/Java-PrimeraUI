    public int obtenerValorCaracteristicas(int posicionCaracteristica) {
        int valorCaracteristica = 0;
        
        switch(posicionCaracteristica) {
            case 0:
                valorCaracteristica += 0;
                break;
            case 1: 
                valorCaracteristica += 500000;
                break;
            case 2:
                valorCaracteristica += 1000000;
                break;
        }
        
        return valorCaracteristica;
    }
    
    public int obtenerValorAccesorio(int posicionAccesorio) {
        int valorCaracteristica = 0;
        
        switch(posicionAccesorio) {
            case 0:
                valorCaracteristica += 0;
                break;
            case 1:
                valorCaracteristica += 60000;
                break;
            case 2:
                valorCaracteristica += 100000;
                break;
            case 3:
                valorCaracteristica += 150000;
                break;
            case 4: 
                valorCaracteristica += 200000;
                break;
            case 5:
                valorCaracteristica += 500000;
                break;
        }
        
        return valorCaracteristica;
    }
    
    
