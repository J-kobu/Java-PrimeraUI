    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        String nombreVehiculo = "";
        String nombreCaracteristica = cbCaracteristicas.getSelectedItem().toString();
        String nombreAccesorio = cbAccesorios.getSelectedItem().toString();
        double automovil = 5000000, camioneta = 8000000, camion = 12000000;
        double valorTotalVehiculo =  0, valorAccesorio = 0, valorCaracteristica = 0;
        int posicionCaracteristica = cbCaracteristicas.getSelectedIndex();
        int posicionAccesorio = cbAccesorios.getSelectedIndex();
        
        if (rbtnAutomovil.isSelected()) {
            nombreVehiculo = "Automóvil";
            valorCaracteristica = obtenerValorCaracteristicas(posicionCaracteristica);
            valorAccesorio = obtenerValorAccesorio(posicionAccesorio);
            valorTotalVehiculo += automovil + valorCaracteristica + valorAccesorio;
            
            JOptionPane.showMessageDialog(null, "Detalle\n"+
                    "\nTipo de vehículo: "+nombreVehiculo+"    Valor: "+automovil+
                    "\nCaracterística    : "+nombreCaracteristica+"    Valor: "+valorCaracteristica+
                    "\nAccesorio          : "+nombreAccesorio+"    Valor: "+valorAccesorio+
                    "\n\nValor total del vehículo: "+(valorTotalVehiculo));
        } else if (rbtnCamioneta.isSelected()) {
            nombreVehiculo = "Camioneta";
            valorCaracteristica = obtenerValorCaracteristicas(posicionCaracteristica);
            valorAccesorio = obtenerValorAccesorio(posicionAccesorio);
            valorTotalVehiculo += camioneta + valorCaracteristica + valorAccesorio;
            
            JOptionPane.showMessageDialog(null, "Detalle\n"+
                    "\nTipo de vehículo: "+nombreVehiculo+"    Valor: "+camioneta+
                    "\nCaracterística    : "+nombreCaracteristica+"    Valor: "+valorCaracteristica+
                    "\nAccesorio          : "+nombreAccesorio+"    Valor: "+valorAccesorio+
                    "\n\nValor total del vehículo: "+(valorTotalVehiculo));
        } else if (rbtnCamion.isSelected()) {
            nombreVehiculo = "Camión   ";
            valorCaracteristica = obtenerValorCaracteristicas(posicionCaracteristica);
            valorAccesorio = obtenerValorAccesorio(posicionAccesorio);
            valorTotalVehiculo += camion + valorCaracteristica + valorAccesorio;
            
            JOptionPane.showMessageDialog(null, "Detalle\n"+
                    "\nTipo de vehículo: " + nombreVehiculo + "    Valor: "+camion +
                    "\nCaracterística    : " + nombreCaracteristica + "    Valor: "+valorCaracteristica+
                    "\nAccesorio          : " + nombreAccesorio + "    Valor: "+valorAccesorio+
                    "\n\nValor total del vehículo: "+(valorTotalVehiculo));
        } else {
            JOptionPane.showMessageDialog(null,"Debe seleccionar el tipo de vehículo");
        }
        
        
    }//GEN-LAST:event_btnImprimirActionPerformed

